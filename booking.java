    public void Load() {

        try {
            SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            String date = df.format(txtdchooser.getDate());

            pst = con.prepareStatement("SELECT seat.launchno,seat.seats,seat.status,seat.date,launchbooking.customer,launchbooking.mobile from seat Left JOIN launchbooking ON seat.launchno=launchbooking.launchno AND seat.seats=launchbooking.seat AND seat.date=launchbooking.date where seat.date = ?");
            pst.setString(1, date);
            rs = pst.executeQuery();
            ResultSetMetaData rsd = rs.getMetaData();
            int c;
            c = rsd.getColumnCount();
            DefaultTableModel d = (DefaultTableModel) jTable1.getModel();
            d.setRowCount(0);
            while (rs.next()) {
                Vector vc = new Vector();
                for (int i = 1; i <= c; i++) {
                    
                    vc.add(rs.getString("launchno"));
                    vc.add(rs.getString("seats"));
                    vc.add(rs.getString("status"));
                    vc.add(rs.getString("customer"));
                    vc.add(rs.getString("mobile"));
                    vc.add(rs.getString("date"));

                }
                d.addRow(vc);

            }

        } catch (SQLException ex) {
            Logger.getLogger(booking.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

