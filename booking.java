    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        DefaultTableModel dtb = (DefaultTableModel) jTable1.getModel();
        int selected = jTable1.getSelectedRow();

        String status = dtb.getValueAt(selected, 2).toString();

        if (!status.equals("Booked")) {

            String seat = dtb.getValueAt(selected, 1).toString();
            String date = dtb.getValueAt(selected, 5).toString();
            txtsno.setText(seat);
            txtdate.setText(date);

        } else {
            JOptionPane.showMessageDialog(this, "Ticket Booked!!!!");
        }

    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {

            DefaultTableModel dtb = (DefaultTableModel) jTable1.getModel();
            int selected = jTable1.getSelectedRow();

            String launchno = dtb.getValueAt(selected, 0).toString();

            String cusname = txtcustomer.getText();
            String seat = txtsno.getText();
            String mno = txtmno.getText();
            String date = txtdate.getText();
            pst = con.prepareStatement("Insert into launchbooking(launchno,seat,customer,mobile,date)values(?,?,?,?,?)");
            pst.setString(1, launchno);
            pst.setString(2, seat);
            pst.setString(3, cusname);
            pst.setString(4, mno);
            pst.setString(5, date);
            pst.executeUpdate();

            pst1 = con.prepareStatement("update seat set status = ? where seats = ?");
            pst1.setString(1, "Booked");
            pst1.setString(2, seat);
            pst1.executeUpdate();
            JOptionPane.showMessageDialog(this, "Launch Seat Booked!!!!");
            Load();

        } catch (SQLException ex) {
            Logger.getLogger(booking.class
                    .getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

